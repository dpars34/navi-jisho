export function jlptRender(param) {
    switch(param) {
        case 'jlpt-n1':
            return 'N1';
        case 'jlpt-n2':
            return 'N2';
        case 'jlpt-n3':
            return 'N3';
        case 'jlpt-n4':
            return 'N4';
        case 'jlpt-n5':
            return 'N5';
      }
}